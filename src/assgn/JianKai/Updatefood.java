    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String thetext = jTextField1.getText();
        if (thetext.isEmpty()) {
             JOptionPane.showMessageDialog(null, "Please fill in Food ID","Error",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            int index = save.findCurMenu(thetext);
            if (index!=0) {
                this.setVisible(false);
                save.setCurMenuForUpdate(save.getCurMenu().get(index));
                Updatefood2 n = new Updatefood2(save);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Food Id not found!","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

