    public boolean checkMenuID(){
        
        for(int i= 0;i<menu.size();i++)
            if(menu.get(i).getMenuID().equals(_menuid.getText()))
            {
                m = menu.get(i);
                return true;
            } 
       
        return false;
    }
    
    public boolean checkOrder(){
        
        for(int i =0;i<order.size();i++){
            if(order.get(i).getFoodid().equals(_menuid.getText()))
                return false;
        }
        
        return true;
    }
        
